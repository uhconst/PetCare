    private void btnSalvar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvar2ActionPerformed
        // Chama a função para verificar se está atualizando ou salvando
        if( servicoControl.checkAtualizandoOuSalvando() == 1 )
        {
            if(JOptionPane.showConfirmDialog( this,
                "Deseja realmente atualizar esse serviço?",
                "Atualizar Serviço", JOptionPane.YES_NO_OPTION) ==
            JOptionPane.YES_OPTION){
            servicoControl.salvar();
            JOptionPane.showMessageDialog( this,
                "Serviço atualizado com sucesso!",
                "Atualizar Serviço", JOptionPane.INFORMATION_MESSAGE );
        }

        }
        else{
            servicoControl.salvar();
            JOptionPane.showMessageDialog( this, "Serviço salva com sucesso!",
                "Salvar Serviço", JOptionPane.INFORMATION_MESSAGE );
        }
    }//GEN-LAST:event_btnSalvar2ActionPerformed

