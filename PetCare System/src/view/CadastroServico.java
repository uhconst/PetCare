    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        tblServicos.clearSelection();

        txtDescricao.setText("");

        // Pesquisando novamente porque senao ele limpa os dados da tabela
        // tambem. Porque estao vinculados com os textFields.
        servicoControl.novo();
        servicoControl.pesquisar();
    }//GEN-LAST:event_btnLimparActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        if(JOptionPane.showConfirmDialog( this, "Deseja realmente excluir essa raça?",
            "Excluir raça", JOptionPane.YES_NO_OPTION) ==
        JOptionPane.YES_OPTION){
        servicoControl.excluir();
        JOptionPane.showMessageDialog( this, "Serviço excluida com sucesso!",
            "Excluir Serviço", JOptionPane.INFORMATION_MESSAGE );
        }
    }//GEN-LAST:event_btnDeletarActionPerformed


